@implementation NSString (YJSString)

+ (NSString *)stringEmpty:(NSString *)emptypString{
    NSString *string = [NSString stringWithFormat:@"%@", emptypString];
    if(string == nil){
        return @"";
    }
    
    if(string == NULL){
        return @"";
    }
    
    if([string isKindOfClass:[NSNull class]]){
        return @"";
    }
    
    if([string isEqualToString:@""] || [string isEqualToString:@"(null)"] || [string isEqualToString:@"<null>"] || [string isEqualToString:@"null"]){
        return @"";
    }
    
    return string;
}

@end

@implementation NSString (YJSBase64)

+ (NSString *)base64Encode:(NSString *)plainText{
    NSData *plainTextData = [plainText dataUsingEncoding:NSUTF8StringEncoding];
    NSString *base64String = [plainTextData base64EncodedString];
    return base64String;
}

+ (NSString *)base64Decode:(NSString *)base64String{
    NSData *plainTextData = [NSData dataFromBase64String:base64String];
    NSString *plainText = [[NSString alloc] initWithData:plainTextData encoding:NSUTF8StringEncoding];
    return plainText;
}

@end

