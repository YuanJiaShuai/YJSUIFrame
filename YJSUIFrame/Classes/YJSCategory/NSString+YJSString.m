        // UniChar ik = (UniChar)[[iK objectAtIndex:i] charValue];
        UniChar ik = (UniChar)[[iK objectAtIndex:i] charValue];
        j = (j + is + ik)%256;
        NSNumber *temp = [iS objectAtIndex:i];
        [iS replaceObjectAtIndex:i withObject:[iS objectAtIndex:j]];
        [iS replaceObjectAtIndex:j withObject:temp];
    }
    
    int i=0;
    j=0;
    
    NSString *result = aInput;
    
